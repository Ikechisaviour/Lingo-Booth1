const mongoose = require('mongoose');

// Append-only per-attempt log. One row per learner interaction with a
// rateable item (Cloze pick, Pattern production attempt, Pronunciation
// score, MinimalPair pick, etc.). Used for SRS tuning, failure-recovery
// triggers, and learner-facing analytics.
const curriculumV2EventSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
  targetLang: {
    type: String,
    required: true,
    maxlength: 20,
  },
  sessionId: {
    type: String,
    maxlength: 80,
    default: '',
  },
  conceptId: {
    type: String,
    required: true,
  },
  lessonId: {
    type: String,
    maxlength: 120,
    default: '',
  },
  lessonType: {
    type: String,
    enum: [
      'PatternLesson',
      'ClozeLesson',
      'StoryLesson',
      'ContrastNote',
      'VocabDeck',
      'PronunciationTask',
      'MinimalPairTask',
    ],
    required: true,
  },
  outcome: {
    type: String,
    enum: ['correct', 'incorrect', 'partial', 'skipped'],
    required: true,
  },
  hintUsed: {
    type: Boolean,
    default: false,
  },
  latencyMs: {
    type: Number,
    min: 0,
    default: null,
  },
  // Populated only when item 22's context boost surfaced this concept.
  // null for all events generated by ordinary session navigation.
  contextSignal: {
    source: { type: String, maxlength: 40, default: '' },
    contextId: { type: mongoose.Schema.Types.ObjectId, ref: 'PracticeContext', default: null },
    signalType: { type: String, enum: ['vocab', 'phrase', 'topic', ''], default: '' },
  },
}, { timestamps: { createdAt: true, updatedAt: false } });

curriculumV2EventSchema.index({ userId: 1, targetLang: 1, createdAt: -1 });
curriculumV2EventSchema.index({ userId: 1, conceptId: 1, createdAt: -1 });
curriculumV2EventSchema.index({ userId: 1, lessonId: 1, createdAt: -1 });

module.exports = mongoose.model('CurriculumV2Event', curriculumV2EventSchema);
